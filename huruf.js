function getLetters(letter){
    switch(letter){
        case "a":return "A";   
        case "i":return "A";   
        case "u":return "A";   
        case "e":return "A";   
        case "o":return "A";   
        case "b":return "B";   
        case "c":return "B";   
        case "d":return "B";   
        case "f":return "B";   
        case "g":return "B";   
        case "h":return "C";   
        case "j":return "C";   
        case "k":return "C";   
        case "l":return "C";   
        case "m":return "C";   
        default:return "D" 
    }
}
console.log(getLetters("o"))